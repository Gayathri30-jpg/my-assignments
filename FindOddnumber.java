package week1.day2;

public class FindOddnumber {

	public static void main(String[] args) {
		int num = 10;
		for (num =1; num<=10 ;num++)
		{
		if (num % 2 ==1)
		{
			System.out.println(num + "number is odd number");
		}	
		else
		{
			System.out.println(num + "numer is even number");
		}
		
	}

}}
